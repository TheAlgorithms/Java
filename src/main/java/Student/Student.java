package Student;

public class Student {
    private String name;
    private int roll;
    private String dept;

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for name
    public String getName() {
        return this.name;
    }

    // Setter for Roll
    public void setRoll(int roll){
        this.roll=roll;
    }

    // Getter for Roll
    public int getRoll(){
        return this.roll;
    }

    // Setter for Department
    public void setDept(String dept){
        this.dept=dept;
    }

    // Getter for Department
    public String getDept(){
        return this.dept;
    }
}
